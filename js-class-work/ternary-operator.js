// Ternary Operators
let firstVar = 10;
let secondVar = 20;

let ternaryOp = (firstVar < secondVar) ? "Answer is true" /* ExpT */ : "Answer is false" /* ExpF */;
/* if the answer is true it executes the first value before the :
if answer is false then it executes the second value after the : */
/* the ? is a must, it is what indicates that is a ternary condition */

console.log(ternaryOp); /* Answer is true */